class Gallery extends Component {
  state = {imageDetails: imagesList[0]}

  onChangeThumbnailImage = imageId => {
    const filterImageList = imagesList.filter(
      eachImage => eachImage.id === imageId,
    )
    // this.setState({imageUrl: filterImageList.imageUrl})
    this.setState({imageDetails: filterImageList[0]})
  }

  render() {
    const {imageDetails} = this.state
    const {imageUrl, imageAltText} = imageDetails

    return (
      <>
        <div className="bg-container">
          <div className="image-container">
            <img src={imageUrl} alt={imageAltText} className="nature-image" />
          </div>
          <h1 className="nature-heading">Nature Photography</h1>
          <p className="photo-by">Nature Photography by Rahul</p>
          <ul>
            {imagesList.map(eachThumbnail => (
              <ThumbnailItem
                imagesList={eachThumbnail}
                key={eachThumbnail.id}
                onChangeThumbnail={this.onChangeThumbnailImage}
                isActive={imageDetails.id === eachThumbnail.id}
              />
            ))}
          </ul>
        </div>
      </>
    )
  }
}
export default Gallery
